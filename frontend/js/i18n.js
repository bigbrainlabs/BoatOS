/**
 * Translate a key. Language is read dynamically from the global system
 * (window.getLanguage from frontend/i18n.js) so a single toggleLanguage()
 * call keeps HTML elements AND JS-rendered strings in sync.
 *
 * @param {string} key  - camelCase key from this module
 * @param {Object} [params] - {placeholder: value} substitutions
 */
export function t(key, params) {
  const lang = (typeof window.getLanguage === 'function') ? window.getLanguage() : 'de';
  const dict = lang === 'en' ? _EN : _DE;
  let str = dict[key];
  if (str === undefined) str = _DE[key];  // German fallback
  if (str === undefined) return key;       // Key not found
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (params[k] !== undefined ? params[k] : `{${k}}`));
}

/**
 * Set language. Delegates to the global setLanguage() which handles
 * localStorage, HTML data-i18n updates, and weather refresh.
 * Also persists to backend so Helm picks it up.
 *
 * @param {string} lang - 'de' or 'en'
 */
export function setLang(lang) {
  if (typeof window.setLanguage === 'function') {
    window.setLanguage(lang);
  }
}

/** Return current language code. */
export function getLang() {
  return (typeof window.getLanguage === 'function') ? window.getLanguage() : 'de';
}
